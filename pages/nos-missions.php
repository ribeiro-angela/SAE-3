<script>
    // Animation des chiffres au scroll
    const observerOptions = {
        threshold: 0.5,
        rootMargin: '0px'
    };

    const animateNumbers = (entries, observer) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                const statNumbers = entry.target.querySelectorAll('.stat-number');
                statNumbers.forEach(stat => {
                    const target = parseInt(stat.getAttribute('data-target'));
                    const duration = 2000;
                    const increment = target / (duration / 16);
                    let current = 0;

                    const updateNumber = () => {
                        current += increment;
                        if (current < target) {
                            stat.textContent = Math.floor(current).toLocaleString('fr-FR');
                            requestAnimationFrame(updateNumber);
                        } else {
                            stat.textContent = target.toLocaleString('fr-FR');
                        }
                    };

                    updateNumber();
                });
                observer.unobserve(entry.target);
            }
        });
    };

    const observer = new IntersectionObserver(animateNumbers, observerOptions);
    const statsSection = document.querySelector('.stats-grid');
    if (statsSection) {
        observer.observe(statsSection);
    }
</script>

